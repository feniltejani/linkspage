export function initializeLinks() {
    const linksContainer = document.getElementById('links');
    
    links.forEach((link, index) => {
        const linkElement = document.createElement('a');
        linkElement.href = link.link;
        linkElement.target = '_blank';
        linkElement.rel = 'noopener noreferrer';
        linkElement.className = 'link';
        linkElement.setAttribute('data-platform', link.name);
        linkElement.setAttribute('data-aos', 'fade-up');
        linkElement.setAttribute('data-aos-delay', (index * 100).toString());
        
        linkElement.innerHTML = `
            <div class="link-content">
                ${link.image}
                <span>${link.name}</span>
            </div>
            <div class="link-arrow">
                <svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24" fill="currentColor">
                    <path d="M16.004 9.414l-8.607 8.607-1.414-1.414L14.589 8H7.004V6h11v11h-2V9.414z"/>
                </svg>
            </div>
        `;

        // Add hover animation
        linkElement.addEventListener('mousemove', (e) => {
            const rect = linkElement.getBoundingClientRect();
            const x = e.clientX - rect.left;
            const y = e.clientY - rect.top;
            
            linkElement.style.setProperty('--mouse-x', `${x}px`);
            linkElement.style.setProperty('--mouse-y', `${y}px`);
        });

        linksContainer.appendChild(linkElement);
    });
}

